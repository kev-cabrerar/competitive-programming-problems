import java.util.Scanner

fun main() {
    val input = Scanner(System.`in`);
    val n = input.nextInt();

    if (n == 61) {
        println("Brasilia")
    } else if (n == 71) {
        println("Salvador")
    } else if (n == 11) {
        println("Sao Paulo")
    } else if (n == 21) {
        println("Rio de Janeiro")
    } else if (n == 32) {
        println("Juiz de Fora")
    } else if (n == 19) {
        println("Campinas")
    } else if (n == 27) {
        println("Vitoria")
    } else if (n == 31) {
        println("Belo Horizonte")
    } else {
        println("DDD nao cadastrado")
    }
    
}
    



